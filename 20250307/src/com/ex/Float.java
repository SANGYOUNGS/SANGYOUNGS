package com.ex;
/*
 *  	실수 : float , double ==> 실수의 기본형은 double
 * 				float 은 값을 마지막에 float를 의미하는 f를 기입함
 * 					
 * 
 * 
 * 
 */
public class Float {

	public static void main(String[] args) {
		
		float var1;
		float var2;
		var1 = 3.4f;
		var2 = (float)55.55;
		
		System.out.println("var 1 의 값 : " + var1); 
		System.out.println("var 2 의 값 : " + var2); 
		
	}

}
