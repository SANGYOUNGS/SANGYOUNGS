package com.inhereex;

public class CellPhone {
	String model; // 모델명
	String number; // 전화번호
	int chord; // 화음
	
	
	public String getModel() {
		return model;
	}
	public void setModel(String model) {
		this.model = model;
	}
	public String getNumber() {
		return number;
	}
	public void setNumber(String n) {
		this.number = n;
	}
	public int getChord() {
		return chord;
	}
	public void setChord(int chord) {
		this.chord = chord;
	}
	
	
	
	
	
	
}
